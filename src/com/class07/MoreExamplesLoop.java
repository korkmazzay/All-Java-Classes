package com.class07;

public class MoreExamplesLoop {

	public static void main(String[] args) {

		
System.out.println("------------------WHATS MY OUTPUT---------------------------");		
		
		for(int a=1; a<=100; a++) {
			System.out.print(a+" ");
		}
		System.out.println();
		System.out.println("------------------WHATS MY OUTPUT---------------------------");		
		
		for(int i=100; i>=1; i--) {
			System.out.print(i+" ");
		}
		System.out.println();
		System.out.println("------------------WHATS MY OUTPUT---------------------------");		
		
		for(int i=20; i>=1; i-=2) {
			System.out.print(i+" ");
		}
	
		System.out.println();
		System.out.println("------------------WHATS MY OUTPUT---------------------------");		
		
		for(int a=21; a<50; a+=2) {
		System.out.print(a+" ");
		
		}	
		System.out.println();
		System.out.println("------------------WHATS MY OUTPUT---------------------------");		
		
		for(int a=20; a<=50; a++) {
		if( a%2==1)
		System.out.print(a+" ");
		}
		
		
	}

}
