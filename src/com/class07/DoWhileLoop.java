package com.class07;

public class DoWhileLoop {

	public static void main(String[] args) {

		/*
		 * DO WHILE LOOPS IN JAVA
		 * WHILE=FIRST CHECK THEN EXECUTE
		 * DO WHILE= FIRST EXECUTE THEN CHECK
		 * EVEN THE CONDITION FALSE DO WHILE WILL EXECUTE THE CODE AT LEAST ONCE
		 */
		
		
	int num1=6;
	do {
		
		System.out.println(num1);
		num1++;
		
	}while(num1<=5);
		
		
	}

}
